export const add = (firstNum: number, secondNum: number): number => firstNum + secondNum

export const subtract = (firstNum: number, secondNum: number) => firstNum - secondNum

export const multiplyTwo = (firstNum: number, secondNum: number): number => firstNum * secondNum

export const multiplyThree = (firstNum: number, secondNum: number, thirdNum: number): number => 
  firstNum * secondNum * thirdNum
